#Adição
soma = 5 + 3
print(soma) ### O resultado deverá ser 8

#Subtração
subtracao = 5 - 3
print(subtracao) ### O resultado deverá ser 2

# Multiplicação
multiplicacao = 5 * 3
print(multiplicacao) ### O resultado deverá ser 15

# Divisão
divisao = 5 / 3
print(divisao) ### O resultado deverá ser 1.6666666666666667

# Divisão Inteira
divisao_inteira = 5 // 3
print(divisao_inteira) ### O resultado deverá ser 1

# Modulo
modulo = 20 % 6
print(modulo) ### O resultado deverá ser 2

#Exponencial
exponencial = 2 ** 3
print(exponencial) ### O resultado deverá ser 8